const { PrismaClient } = require("@prisma/client");
const fs = require("node:fs/promises");
const path = require("node:path");
const readline = require("node:readline/promises");
const { stdin: input, stdout: output } = require("node:process");

const prisma = new PrismaClient();
const CONFIRM_TEXT = "BACKUP DATABASE";

function getArgValue(flag) {
  const idx = process.argv.indexOf(flag);
  if (idx >= 0 && idx + 1 < process.argv.length) {
    return process.argv[idx + 1];
  }
  return null;
}

function escapeSqlString(value) {
  return String(value).replace(/'/g, "''");
}

function toSqlLiteral(value) {
  if (value === null || value === undefined) return "NULL";
  if (typeof value === "number") return Number.isFinite(value) ? String(value) : "NULL";
  if (typeof value === "boolean") return value ? "TRUE" : "FALSE";
  if (value instanceof Date) return `'${escapeSqlString(value.toISOString())}'`;
  if (typeof value === "object") return `'${escapeSqlString(JSON.stringify(value))}'`;
  return `'${escapeSqlString(value)}'`;
}

function quoteIdent(identifier) {
  return `"${String(identifier).replace(/"/g, '""')}"`;
}

function buildInsert(table, row) {
  const columns = Object.keys(row);
  if (!columns.length) return null;
  const values = columns.map((col) => toSqlLiteral(row[col]));
  return `INSERT INTO ${quoteIdent(table)} (${columns.map(quoteIdent).join(", ")}) VALUES (${values.join(", ")});`;
}

function buildTimestamp() {
  const now = new Date();
  const yyyy = now.getFullYear();
  const mm = String(now.getMonth() + 1).padStart(2, "0");
  const dd = String(now.getDate()).padStart(2, "0");
  const hh = String(now.getHours()).padStart(2, "0");
  const mi = String(now.getMinutes()).padStart(2, "0");
  const ss = String(now.getSeconds()).padStart(2, "0");
  return `${yyyy}${mm}${dd}-${hh}${mi}${ss}`;
}

async function askConfirmation() {
  const argValue = getArgValue("--confirm");
  if (argValue) return argValue;

  const rl = readline.createInterface({ input, output });
  const answer = await rl.question(
    `This will export all data to SQL INSERT statements. Type \"${CONFIRM_TEXT}\" to continue: `
  );
  rl.close();
  return answer.trim();
}

async function main() {
  const answer = await askConfirmation();
  if (answer !== CONFIRM_TEXT) {
    console.error("Confirmation text mismatch. Aborted.");
    process.exitCode = 1;
    return;
  }

  const [
    tournaments,
    maps,
    heroes,
    teams,
    members,
    matches,
    draftTables,
    draftActions,
    news,
    playerStats,
    allowedMapsJoin,
  ] = await Promise.all([
    prisma.tournament.findMany({ orderBy: { id: "asc" } }),
    prisma.map.findMany({ orderBy: { id: "asc" } }),
    prisma.hero.findMany({ orderBy: { id: "asc" } }),
    prisma.team.findMany({ orderBy: { id: "asc" } }),
    prisma.member.findMany({ orderBy: { id: "asc" } }),
    prisma.match.findMany({ orderBy: { id: "asc" } }),
    prisma.draftTable.findMany({ orderBy: { id: "asc" } }),
    prisma.draftAction.findMany({ orderBy: { id: "asc" } }),
    prisma.news.findMany({ orderBy: { id: "asc" } }),
    prisma.playerStat.findMany({ orderBy: { id: "asc" } }),
    prisma.$queryRawUnsafe('SELECT * FROM "_AllowedMaps" ORDER BY "A", "B"'),
  ]);

  const lines = [];
  lines.push("-- Full data backup generated by db-backup-inserts.js");
  lines.push(`-- Generated at ${new Date().toISOString()}`);
  lines.push("BEGIN;");
  lines.push(
    'TRUNCATE TABLE "PlayerStat", "DraftAction", "DraftTable", "News", "Match", "Member", "Team", "Tournament", "Hero", "Map", "_AllowedMaps" RESTART IDENTITY CASCADE;'
  );

  const orderedTables = [
    ["Tournament", tournaments],
    ["Map", maps],
    ["Hero", heroes],
    ["Team", teams],
    ["Member", members],
    ["Match", matches],
    ["_AllowedMaps", allowedMapsJoin],
    ["DraftTable", draftTables],
    ["DraftAction", draftActions],
    ["News", news],
    ["PlayerStat", playerStats],
  ];

  for (const [table, rows] of orderedTables) {
    lines.push(`-- ${table}`);
    for (const row of rows) {
      const statement = buildInsert(table, row);
      if (statement) lines.push(statement);
    }
  }

  lines.push("COMMIT;");

  const backupDir = path.join(process.cwd(), "backups");
  await fs.mkdir(backupDir, { recursive: true });

  const fileName = `db-backup-${buildTimestamp()}.txt`;
  const outputPath = path.join(backupDir, fileName);
  await fs.writeFile(outputPath, `${lines.join("\n")}\n`, "utf8");

  console.log(`Backup written to: ${outputPath}`);
  console.log("Use psql to restore this file if needed.");
}

main()
  .catch((error) => {
    console.error("Failed to generate backup:", error?.message || error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
